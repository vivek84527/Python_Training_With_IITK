d = {'A': 'Java', 'C': 'Android', 'B': 'J2EE',
     'D': 'Python', 'E': 'Hadoop', 'Key': 'Value'}

print("d.keys() = ", d.keys())

print("sorted( d.keys() ) = ", sorted(d.keys()))
print("sorted( d.keys() ) = ", sorted(d.keys(), reverse=True))

print("d.values() = ", d.values())

print("d.items() = ", d.items())
