# Set - Unordered Collection of unique and immutable objects
# Set can be created by calling in-built function- set()

set1 = set('ttrryy')
set2 = set('ccrryy')
print("set1 = ", set1)
print("set2 = ", set2)

# Dumping sets
# Union
print(" set1 | set2 = ", set1 | set2)

# Intersection
print(" set1 & set2 = ", set1 & set2)

# Difference
print(" set1 - set2 = ", set1 - set2)
